$('#submitUrl').click(function(e){
    e.preventDefault();

    var bigUrl = $('#bigUrl').val(),
        message$ = $('#message'),
        urlOut$ = $('#urlOut');

    // Reset error and URL
    message$.html('').hide();
    urlOut$.html('').hide();

    // Check if there is a URL
    if (!bigUrl) {
        message$.addClass('danger alert').html('Oops... We seem to be missing the URL?').show();
        return;
    }

    // Check if the URL is a valid one
    if (!validURL(bigUrl)) {
        message$.addClass('danger alert').html('You need to give me a valid URL so I can minify it.').show();
    }

    data = {
        bigUrl: bigUrl
    };

    // All valid so send AJAX request
    $.ajax({
        dataType: "json",
        url: 'php/url-processor.php',
        type: "POST",
        data: data,
        success: function(response) {
            if (response.success === true) {
                urlOut$.addClass('field success').html('<input id="urlOutInput" class="input" type="text" value="'+ response.littleUrl + '" />').show();
            } else {
                message$.addClass('danger alert').html('Unknown error: '+ response.message +'').show();
            }
        }
    });
});

